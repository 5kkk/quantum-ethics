			<h4>Preface</h4>
			<br />
			<p>The birth of Quantum Physics in the 1920s has been marked by a long period of intense controversies about its interpretation, which has been recently reviewed by Juan Miguel Marin in his paper <i>‘Mysticism’ in quantum mechanics: the forgotten controversy</i>. The Copenhagen interpretation which emerged from these debates has been seemingly dominating the scene in the 1950s-1960s, not because it was intrinsically better than others, but because it seemed to challenge the materialist world view of classical, "everyday" physics as little as possible. Since the 1970s, however, numerous alternative interpretations have been proposed and older interpretations further developed: Pilot-Wave Theory, Many-Worlds interpretation, Many-Minds interpretation, Dynamical Collapse Theories, Decoherent Histories... All these attempts to give Quantum Physics a sound interpretation are facing the problem that the mathematical theory itself, in the form of the Standard Model of Quantum Field Theory (or of slight variants regarding the existence of the Higgs field, of neutrino masses...), is ill-defined, and that it is therefore impossible to assign a physical or metaphysical meaning to the fundamental mathematical entities of the theory. Of course, it is possible to choose a specific, mathematically well-defined regularization of the theory to this purpose, but since renormalization methods are leading, independently of the chosen regularization, to the same results in the singular limit of the original theory, we don't know which regularization is the right one, and therefore we don't know either which are the fundamental mathematical entities to be interpreted. Quite surprisingly, however, it seems that this issue has never been accounted for yet. Existing interpretations of Quantum Physics are either restricted to non-relativistic Quantum Mechanics, which is no fundamental theory, or are formulated excessively vaguely, which has made most "serious" physicists doubt that such an interpretation is possible at all. This book will have reached his goal if it convinces the reader of the contrary and helps interpretation issues recovering again their place at the heart of the research on Quantum Field Theory. For this purpose, I shall take as an example a lattice regularization scheme, formulate in this well-defined framework a rather conservative interpretation inspired by Spinoza's philosophy and show that classical philosophical questions can be formulated in the frame of the resulting naturalistic metaphysics.</p>
